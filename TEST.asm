.MODEL SMALL
.STACK 100H 
.DATA

MSG1 DB 'ENTER THE FIRST NUMBER $' 
MSG2 DB 'ENTER THE SECOND NUMBER $'
MSG3 DB 'ENTER THE THIRD NUMBER $'
MSG4 DB 'THE RESULT $'

.CODE

MAIN PROC
    ;INIT DATA SEGMENT
    MOV AX,@DATA
    MOV DS,AX

    ;PRINT THE FIRST MSG
    MOV AH,9
    LEA DX, MSG1
    INT 21H

    ;TAKE THE FIRST NUMBER
    MOV AH,1
    INT 21H
    MOV BL,AL

    ;CREATE THE NEW LINE
    MOV AH,2
    MOV DL,10

    ;PRINT THE FIRST MSG
    MOV AH,9
    LEA DX, MSG2
    INT 21H




    MAIN ENDP
END MAIN









