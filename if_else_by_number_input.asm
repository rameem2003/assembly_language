;IF ELSE PROGRAM
;CHECK THE NUMBER POSITIVE OR NEGATIVE BY
;TAKE A NUMBER FROM USER

INCLUDE 'EMU8086.INC'
.MODEL SMALL
.STACK 100H
.DATA

MSG DB 'ENTER THE NUMBER: $'

.CODE

MAIN PROC     
    ;DATA SEGMENT INIT
    MOV AX, @DATA
    MOV DS, AX
    
    ;PRINT THE MSG
    MOV AH,9
    LEA DX, MSG
    INT 21H
    
    ;TAKE THE NUMBER FROM USER
    MOV AH,1
    INT 21H
    MOV BL, AL  
    
    ;CREATE THE NEW LINE
    MOV AH,2
    MOV DL,10
    INT 21H
    MOV DL, 13
    INT 21H
    
    
    ;NOW COMPARE THE NUMBER IS IT LESS THAN 0 OR NOT
    CMP BL, 0
    
    JL IF
    JGE ELSE
    
    IF:
    PRINTN "THE NUMBER IS NEGATIVE" 
    JMP END_IF
    
    
    ELSE:
    PRINTN "THE NUMBER IS POSITIVE"
    JMP END_IF
    
    
    
    END_IF:
    
    MOV AH, 4CH
    INT 21H
    MAIN ENDP
END MAIN